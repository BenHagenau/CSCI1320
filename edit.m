clear all; 
x=zeros(256,256,3);

b=zeros(256,256,3);
for i=(1:256)
    b(:,i,1)=1-((i-1)/255);
    b(:,i,2)=1-((i-1)/255);
    b(:,i,3)=1-((i-1)/255);
end

r=zeros(256,256,3);
for i=(1:256)
    r(i,:,1)=0+((i-1)/255);
    r(i,:,2)=0+((i-1)/255);
    r(i,:,3)=0+((i-1)/255);
    
end

x=r.*b;
imagesc(x)